public class Client {
    private Integer id;
    private String name;
    private String lastName;
    private Integer age;
    private Boolean isNiga;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getLastName() {
        return lastName;
    }

    public int getAge() {
        return age;
    }
    public boolean getIsNiga(){
        return isNiga;
    }
}
